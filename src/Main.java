public class Main {
    public static void main(String[] args) {
        int x = 25, y = 10;
        System.out.println("x = 25");
        System.out.println("x = 25");

        System.out.println("x + y = " + (x + y));
        System.out.println("x - y = " + (x - y));
        System.out.println("x * y = " + (x * y));
        System.out.println("x / y = " + (x / y));
    }
}